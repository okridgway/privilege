        <h1>Putting Privilege into Perspective</h1>
        <p><strong><em>A visual essay about poverty, power, and perception.</em></strong></p>
        <p>We&#39;re the middle-class—college educated—9-5—white-collar workers, who are frustrated by slow internet, and expired coupon codes. Our bad days consist of workplace stress, ebarassing situations, and traffic jams—but rarely do we take the time to appreciate just how fortunate we are. </p>
        <p>In some form or another, we are all privileged. But what does this privilege really look like? The goal of this essay is to tell the story of the middle class americans through a lens of privilege which focuses on what we <em>do</em> have, rather than what we <em>don&#39;t</em> have. </p>
        <p>While the easiest way to write this essay would be to simply research and spout statistics showing &#39;conclusive evidence&#39; that we are &#39;indeed more afluent than those in other corners of the world&#39; numbers don&#39;t do much for us. Hell, some even believe that <a href='https://io9.gizmodo.com/how-to-comprehend-incomprehensibly-large-numbers-1531604757'>anything larger than 100</a> is only interpreted as &quot;big&quot; in our minds; which means that a black and white piece of paper (or screen) will have little to no impact as far as the eye-opening global awareness that we are after goes. </p>
        <p><strong>That being said, here is the plan:</strong></p>
        <ol start=''>
            <li>Read about data visualization and how it impacts our comprehension and understanding.</li>
            <li>Read about 1st world privilege and various ways that we are privileged.</li>
            <li>Lay out basic storyline of essay and what statistics should be included.</li>
            <li>Find specified statistics needed as outlined in step 3.</li>
            <li>Design layout of visual essay and determine what visualizations will look like.</li>
            <li>Code visual essay and bring it all together.</li>

        </ol>

